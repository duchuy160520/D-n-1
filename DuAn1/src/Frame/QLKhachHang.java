    public boolean check() {
        String makh = this.txtMaKh.getText();
        String hoten = this.txtTen.getText();

        if (makh.length() == 0 || hoten.length() == 0) {
            JOptionPane.showMessageDialog(this, "không được để trống ");
            return false;
        }
        KhachHang kh = new KhachHang(makh, hoten);
        return true;
    }
    

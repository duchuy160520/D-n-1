    public void hienThiTable() {
        this.listKH = this.qlkh.getAll();
        DefaultTableModel dtm = (DefaultTableModel) this.tblKhachHang.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < listKH.size(); i++) {
            KhachHang kh = listKH.get(i);

            Object[] row = new Object[]{
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getSdt(),
                kh.getGioiTinh(),
                kh.getNgaySinh()
            };
            dtm.addRow(row);
        }
    }

